#include<stdio.h>
int main()
{

		int a=10,b=4,res;
	

		/*printing a and b*/	
		printf("a is %d and b is %d\n",a,b);

		/*addition*/
		res=a+b;
		printf("a+b is %d\n",res);
		
		/*substraction*/
		res=a-b;
		printf("a-b is %d\n",res);
		
		/*multiplication*/
		res=a*b;
		printf("a*b is %d\n",res);

		/*division*/		
		res=a/b;
		printf("a/b is %d\n",res);
		
		/*modulus*/		
		res=a%b;
		printf("a%%b is %d\n",res);
		



		return 0;

}
